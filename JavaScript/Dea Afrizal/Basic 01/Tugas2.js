//Menentukan hari

let hari = new Date().getDay()
switch (hari) {
    case 1 :
        hari = "senin"
        break
    case 2 :
        hari = "Selasa"
        break;
    case 3 :
        hari = "Rabu"
        break;
    case 4 :
        hari = "Kamis"
        break;
    case 5 :
        hari = "Jumat"
        break;
    case 6 :
        hari = "Sabtu"
        break;
    case 7 :
        hari = "Minggu"
        break;
} 
console.log(`Hari ini adalah hari ${hari}`)

